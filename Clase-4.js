// Módulo OS (operating system)

const os = require('os');

// Type para ver el tipo de sistema operativo
console.log(os.type());

// Homedir para directorio principal 
console.log(os.homedir());

// Uptime para ver el tiempo de uso del sistema operativo
console.log(os.uptime());

// UserInfo para ver la información del usuario 
console.log(os.userInfo()); 