// Modelo cliente-servidor (HTTP client)

/*El modelo cliente-servidor es un modelo de arquitectura de software en el que un cliente realiza solicitudes a un servidor para obtener recursos o servicios. En este modelo, el servidor es responsable de proporcionar los recursos o servicios solicitados por el cliente.

El cliente-servidor se compone de dos componentes principales: el cliente y el servidor. El cliente es la aplicación o dispositivo que solicita los servicios o recursos, mientras que el servidor es la aplicación o dispositivo que proporciona los servicios o recursos.

En este modelo, el cliente y el servidor se comunican a través de una red utilizando protocolos de comunicación estándar como HTTP, TCP/IP, UDP, etc. Cuando el cliente realiza una solicitud al servidor, el servidor procesa la solicitud y envía la respuesta al cliente.

El modelo cliente-servidor se utiliza ampliamente en la creación de aplicaciones web, sistemas de bases de datos, sistemas de correo electrónico, juegos en línea y muchas otras aplicaciones. Una de las ventajas de este modelo es que permite la escalabilidad y el rendimiento, ya que el servidor puede manejar múltiples solicitudes de múltiples clientes al mismo tiempo. También permite la separación de responsabilidades entre el cliente y el servidor, lo que puede simplificar el desarrollo y mantenimiento de la aplicación */

